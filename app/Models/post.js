class Post {
    constructor(title, description, body, status, featured, image, tags, created_date, author) {
        this.title = title;
        this.description = description;
        this.body = body;
        this.status = status;
        this.featured = featured;
        this.author = author;
        this.created_date = created_date;
        this.image = image;
        this.tags = tags;
    }
}